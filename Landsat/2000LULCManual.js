var bands = ['B1', 'B2', 'B3', 'B4', 'B5', 'B6','B7'];
var sample = classNames; 
// print(training);
var sample = sample.randomColumn();
print(sample)
var traininggcp = sample.filter(ee.Filter.lt('random', 0.50))// //70% training and 30% validation
var validation = sample.filter(ee.Filter.gte('random', 0.50));
print(traininggcp)
print(validation)
var training = collection.select(bands).sampleRegions({
  collection: traininggcp,
  properties: ['lulc'],
  scale: 20
});
var classifier = ee.Classifier.libsvm().train({ //ee.Classifier.decisionTree(treeString)
  features: training,
  classProperty: 'lulc',
  inputProperties: bands
});
//Run the classification
var classified = collection.select(bands).classify(classifier);
//Display classification
Map.centerObject(classNames, 10);
Map.addLayer(classified,
{min: 1, max: 7,palette: ['0cffff','27a910','81937e','d61717','f1fb5b','0cff08','cf1ad2']},
'classification',0);

var test = classified.sampleRegions({
  collection: validation,
  properties: ['lulc'],
    scale:20 ,
});

var testConfusionMatrix = test.errorMatrix('lulc', 'classification')
// Printing of confusion matrix may time out. Alternatively, you can export it as CSV
